// Que 1, print all even numbers from 0 to 100

for(let i=1;i<=100;i++){
    if(i%2===0){
        document.write("even num = ",i +"<br>");
    }
    
}

//Que 2, print all odd number from 0 to 100

for(let num=0; num<=100;num++){
    if(num%2!==0){
        document.write("odd num= ",num+"<br>");
    }
}